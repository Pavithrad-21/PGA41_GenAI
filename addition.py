def add_two_numbers(a, b):
    """
    Function to add two numbers
    
    Args:
        a: First number
        b: Second number
    
    Returns:
        The sum of a and b
    """
    return a + b


if __name__ == "__main__":
    # Example usage
    num1 = 10
    num2 = 20
    result = add_two_numbers(num1, num2)
    print(f"The sum of {num1} and {num2} is {result}")
